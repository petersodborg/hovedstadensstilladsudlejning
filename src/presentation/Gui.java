    private void jButton_addteamtoOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addteamtoOrderActionPerformed

        control.droperrorReportTeam();
        control.dropTeamSearch();
        String datoStart = dateChooserCombo_planlaegstartdato.getText();
        String datoSlut = dateChooserCombo_planlaeg_slutdato.getText();

        // omformateret year-mm-dd
        String datoslutny = "";
        String datostartny = "";
        int ordreId = 0;

        try {
            datoslutny = control.omformaterDato(datoSlut);
            datostartny = control.omformaterDato(datoStart);
            ordreId = Integer.parseInt(jTextField_soegPaaOrdrevedplanlaeg.getText());
        } catch (Exception e) {
            jLabel_planlaeg_tilfoejteam.setText("Husk at tilføj dato og ordre.");
            datoslutny = null;
            datostartny = null;
        }

        List listofTeams = null;
        listofTeams = jList_planlaegteam.getSelectedValuesList();
        if (listofTeams.size() == 0) {
            jLabel_planlaeg_tilfoejteam.setText("Fejl, Husk at vælg mindst et team.");
        } else if (control.dateValidate(datostartny, datoslutny)) {
            JOptionPane.showMessageDialog(null, "Tjek om din slut dato er mindre end din startdato. \n Eller om dine datoer er ens.");
            jLabel_planlaeg_tilfoejteam.setText("Fejl tjek dato");
        } else {

            //får en liste og laver det til et string array
            String[] array = new String[listofTeams.size()];
            for (int i = 0; i < listofTeams.size(); i++) {
                array[i] = (String) listofTeams.get(i); 
            }
            String fejlbesked = "";
            TeamOnOrdre tOO = null;

            for (int i = 0; i < control.getTeams().size(); i++) {
                for (int j = 0; j < array.length; j++) {
                    // hvis db teams er lig med string array så oprreter et hold på en ordre i db
                    if (control.getTeams().get(i).getNavn().equalsIgnoreCase(array[j])) {

                        control.createTeamOnOrder(0, datostartny, datoslutny, ordreId, control.getTeams().get(i).getId());

                    }
                }
            }

            control.checkIfTeamIsAvailable();
            if (control.geterrorReportTeam().size() == 0) {
                model2.removeAllElements();
                control.saveTeamOnOrder();
                control.dropTeamOnOrdre();
                jLabel_planlaeg_tilfoejteam.setText("Holdet er nu tilføjet");
                for (int i = 0; i < control.SearchafterTeamsOnOrder(ordreId).size(); i++) {
                    model2.addElement(control.getTeamsearch().get(i).getNavn());
                    jList_soegpaaordre_Teams.setModel(model2);

                }

            } else {
                for (int i = 0; i < control.geterrorReportTeam().size(); i++) {
                    fejlbesked += control.geterrorReportTeam().get(i).toString() + "\n";
                }
                JOptionPane.showMessageDialog(null, fejlbesked);
                fejlbesked = "";
                control.dropTeamOnOrdre();
            }

        }
    }//GEN-LAST:event_jButton_addteamtoOrderActionPerformed

